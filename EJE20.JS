function multiplosDe3Hasta50() {
    let i = 3;
  
    console.log("Ejercicio 20 - Múltiplos de 3 menores a 50:");
  
    while (i < 50) {
      console.log(i);
      i += 3; // Ir sumando de 3 en 3
    }
  }
  
  // ✅ Llamar a la función
  multiplosDe3Hasta50();
  